package day02;

public class Day02Variables {
    public static void main(String[] args) {
        System.out.println(age);
        System.out.println(height);
        System.out.println(weight);

        //If you create variable inside "main" metode

        //Create a char variable
        //Do not forget to put the value between single quotes
        //Default value of char is the character whose value is zero
        char initial ='S';

        //Create a boolean variable
        //Default value of boolean is false
        boolean isOld=true;
        boolean isAlive=false;

        //Create a byte variable
        byte ageOfStd = 13;

        //Create a short variable
        short populationOfVillage = 21000;

        //Create a long variable
        //Do not forget to put L to end when you create a long variable
        long populationOfWorldVillage = 7123456789L;

    }
    //How to create a variable
    // Data Type - Variable Name - Equal Sign - Value - Semi Colon
    //Create an integer variables
    static int age = 12;
    static int height = 183;
    //Create a double variable
    static double weight  = 85.7;
    //Note: If you do not type any access modifier, it means the access modifier is "default"


    String nameOfStudents = "Mark Twain";
    String stdId = "Mt2022_2";
}

